/*
"typedef:"
typedef is used to create a user-defined data type 
alias in C. It allows you to define a new name for 
an existing data type, which can make your code 
more readable and maintainable.
----------------------------------------------
"struct s_list:"
This line defines a structure named s_list. 
A structure is a user-defined data type that can 
hold multiple variables of different data types 
under a single name.
----------------------------------------------
void is used to store content(data) associated 
with each node in the linked list.
----------------------------------------------
"struct s_list *next" points to the next node
in the linked list, creating a linkage.
or NULL if the next node is the last one.
refer to day 12 of notebok.
*/
typedef struct s_list
{
	void					*content;
	struct s_list			*next;
}	t_list;
