#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	index;

	index = 0;
	while (s[index])
	{
		write(fd, &s[index], 1);
		index++;
	}
}
//Description
//Outputs the string ’s’ to the given file descriptor(fd).
//Parameters:
//s: The string to output.
//fd: The file descriptor on which to write.
